int		ft_ll_intlen(long long int nb)
{
	int i;

	i = 1;
	if (nb == -9223372036854775807)
		return (20);
	if (nb < 0)
	{
		nb = -nb;
		i++;
	}
	while (nb >= 10)
	{
		nb = nb / 10;
		i++;
	}
	return (i);
}
