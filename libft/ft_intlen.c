int		ft_intlen(int nb)
{
	int i;

	i = 1;
	if (nb == -2147483648)
		return (11);
	if (nb < 0)
	{
		nb = -nb;
		i++;
	}
	while (nb >= 10)
	{
		nb = nb / 10;
		i++;
	}
	return (i);
}
